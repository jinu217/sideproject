<script>
  // 뒤로가기 아이콘
  var arrowBackIosNewIcon = document.getElementById("arrowBackIosNewIcon");
  if (arrowBackIosNewIcon) {
    arrowBackIosNewIcon.addEventListener("click", function (e) {
      // 필요하면 이전 단계 이동
      window.history.back();
    });
  }

  // 다음 버튼
  var nextBtn = document.getElementById("container");

  // ============================
  //   칩 선택 로직
  // ============================

  // 1) 단일 선택 그룹 (참여 방식 / 주간 빈도 / 시간대 / 일정 조정 가능 여부)
  const singleGroups = document.querySelectorAll(".chip-group.chip-single");
  singleGroups.forEach(group => {
    const chips = group.querySelectorAll(".wrapper6, .wrapper25");
    chips.forEach(chip => {
      chip.addEventListener("click", () => {
        // 그룹 내 다른 칩들 해제
        chips.forEach(c => c.classList.remove("active"));
        // 현재 칩만 활성화
        chip.classList.add("active");

        // ✅ 일정 조정 가능 여부 그룹이면 다음 버튼 활성화
        if (group.classList.contains("frame-parent13") && nextBtn) {
          nextBtn.classList.add("active");
        }
      });
    });
  });

  // 2) 요일 (복수 선택)
  const weekdayChips = document.querySelectorAll(".weekday-chip");
  weekdayChips.forEach(chip => {
    chip.addEventListener("click", () => {
      chip.classList.toggle("active");
    });
  });

  // 3) 다음 버튼 클릭 (active일 때만 동작)
  if (nextBtn) {
    nextBtn.addEventListener("click", function () {
      if (!nextBtn.classList.contains("active")) return;

      window.location.href = "04_04_new_project.php";
    });
  }
</script>